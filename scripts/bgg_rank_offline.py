def main():
    os.chdir(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

    # Load existing game IDs
    existing = set()
    for f in os.listdir(CONTENT_DIR):
        if f.endswith(".json") and f != "_template.json":
            existing.add(f.replace(".json", ""))

    # Read games list
    with open(GAMES_FILE, "r", encoding="utf-8") as f:
        games = [line.strip() for line in f if line.strip()]

    print(f"Loaded {len(games)} games, {len(existing)} already have JSONs")

    # Build ranked list
    ranked = []
    not_found = []
    for title in games:
        if title in BGG_DATA:
            data = BGG_DATA[title]
            rank = data[0]
            game_id = make_game_id(title)
            ranked.append({
                "original_title": title,
                "game_id": game_id,
                "bgg_rank": rank,
                "avg_rating": data[1],
                "weight": data[2],
                "min_players": data[3],
                "max_players": data[4],
                "min_time": data[5],
                "max_time": data[6],
                "year_published": data[7],
                "publisher": data[8],
                "mechanics": list(data[9]),
                "categories": list(data[10]),
            })
        else:
            not_found.append(title)

    if not_found:
        print(f"\nWARNING: {len(not_found)} games not in BGG_DATA:")
        for g in not_found:
            print(f"  - {g}")

    # Sort by rank
    ranked.sort(key=lambda x: x["bgg_rank"])

    # Top 100
    top100 = ranked[:100]

    print(f"\nTop 100 games by BGG rank:")
    for i, g in enumerate(top100):
        rank_str = str(g["bgg_rank"]) if g["bgg_rank"] < 99999 else "NR"
        print(f"  {i+1:3d}. [{rank_str:>6s}] {g['original_title']} (rating: {g['avg_rating']:.1f}, weight: {g['weight']:.1f})")

    # Save outputs
    os.makedirs(os.path.dirname(OUTPUT_JSON), exist_ok=True)

    with open(OUTPUT_JSON, "w", encoding="utf-8") as f:
        json.dump(top100, f, indent=2, ensure_ascii=False)
    print(f"\nSaved {OUTPUT_JSON}")

    with open(OUTPUT_NAMES, "w", encoding="utf-8") as f:
        for g in top100:
            f.write(g["original_title"] + "\n")
    print(f"Saved {OUTPUT_NAMES}")

    # Create base game JSONs
    print(f"\n=== Creating base game JSONs ===")
    created = 0
    skipped = 0
    for g in top100:
        game_id = g["game_id"]
        json_path = os.path.join(CONTENT_DIR, f"{game_id}.json")
        if game_id in existing:
            print(f"  SKIP (exists): {game_id}")
            skipped += 1
            continue

        data = BGG_DATA[g["original_title"]]
        base = create_base_json(g["original_title"], game_id, data)
        with open(json_path, "w", encoding="utf-8") as f:
            json.dump(base, f, indent=2, ensure_ascii=False)
        created += 1
        print(f"  Created: {game_id}.json")

    print(f"\n{'='*60}")
    print(f"PHASE 1 COMPLETE")
    print(f"  Games in list:     {len(games)}")
    print(f"  BGG data found:    {len(ranked)}")
    print(f"  Not in dataset:    {len(not_found)}")
    print(f"  Top 100 saved:     {OUTPUT_JSON}")
    print(f"  Names saved:       {OUTPUT_NAMES}")
    print(f"  JSONs created:     {created}")
    print(f"  JSONs skipped:     {skipped} (already exist)")
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
