def make_game_id(title):
    """Convert title to lowercase hyphenated game_id."""
    s = title.lower()
    s = re.sub(r'\([^)]*\)', '', s)
    s = re.sub(r'\bw/\s+.*?(expansion|exp)\b', '', s, flags=re.IGNORECASE)
    s = re.sub(r"[''']", '', s)
    s = re.sub(r'[^a-z0-9\s-]', ' ', s)
    s = re.sub(r'\s+', '-', s.strip())
    s = s.strip('-')
    s = re.sub(r'-+', '-', s)
    return s


def weight_to_complexity(w):
    if w < 1.5:
        return "party"
    elif w < 2.5:
        return "gateway"
    elif w < 3.5:
        return "midweight"
    else:
        return "heavy"


